const controller = require('../../controllers/website/website.trackEvent.controller')

module.exports = function (app) {
  app.use(function (req, res, next) {
    res.header(
      'Access-Control-Allow-Headers',
      'x-access-token, Origin, Content-Type, Accept'
    )
    next()
  })

  app.post('/website/trackEvent', controller.trackEvent)

}
